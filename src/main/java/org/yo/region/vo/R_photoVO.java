package org.yo.region.vo;

public class R_photoVO {

	private Integer no;
	private String dir;
	private String regionname;
	private String content;
	
	public Integer getNo() {
		return no;
	}
	public R_photoVO setNo(Integer no) {
		this.no = no;
		return this;
	}
	public String getDir() {
		return dir;
	}
	public R_photoVO setDir(String dir) {
		this.dir = dir;
		return this;
	}
	public String getRegionname() {
		return regionname;
	}
	public R_photoVO setRegionname(String regionname) {
		this.regionname = regionname;
		return this;
	}
	public String getContent() {
		return content;
	}
	public R_photoVO setContent(String content) {
		this.content = content;
		return this;
	}
	
	
	
	
	
}
